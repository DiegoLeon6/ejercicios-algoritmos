import math

raiz = math.sqrt

print("Digite A conrinuación los datos requeridos para hayar la hpotenusa del triángulo rectángulo dado: ")

Cateto1 = int (input ("Digite el cateto 1: "))
Cateto2 = int (input("Digite el cateto 2: "))

hipotenusa = raiz (Cateto1**2 + Cateto2**2)

print ("La hipotenusa del  triángulo rectángulo calculado a partir del TEOREMA DE PITAGORAS es: ", hipotenusa)