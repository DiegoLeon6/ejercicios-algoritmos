
import math
num = float (input("Ingresa un numero: "))


sen = math.sin (num)
cos = math.cos(num)
tan = math.tan(num)
raizCuadrada = math.sqrt(num)
Log = math.log(num)

print ("Del numero: ", num)
print ("Su seno es: ", sen)
print ("Su coseno es: ", cos)
print ("Su tangente es: ", tan)
print ("Su raiz cuadrada es: ", raizCuadrada)
print ("El logaritmo natural es: ", Log)
