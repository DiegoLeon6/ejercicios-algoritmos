print ("A continuación digite los datos requeridos, y al final se mostrara el área y el perimetro del rectangulo dado.")

altura =  int (input ("Digite la medida de la altura: "))

base = int (input ("Digite la medida de la base: "))

area = base * altura

print ("Segun los datos el area es: ", area)

perimetro = 2 * (base + altura)

print ("Y su perimetro es: ", perimetro)
